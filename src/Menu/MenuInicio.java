    private void botonOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonOKActionPerformed
        PanelDeMenu panel = menus.get(posicionMostrandose);
        boolean sePudoEjecutar = panel.ejecutar();
        if (sePudoEjecutar) {
            //Llamar el siguiente menú en la lista
            if (posicionMostrandose < menus.size() - 1) {
                CardLayout cd = (CardLayout) opciones.getLayout();
                cd.show(opciones, menus.get(++posicionMostrandose).getName());
            } //Si se ha llegado al final de la lista, notificar final de ejecución
            else {
                terminar();
            }
        }
    }//GEN-LAST:event_botonOKActionPerformed

    private void botonAtrasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAtrasActionPerformed
        if (posicionMostrandose > 0) {
            CardLayout cd = (CardLayout) opciones.getLayout();
            cd.show(opciones, menus.get(--posicionMostrandose).getName());
        }
    }//GEN-LAST:event_botonAtrasActionPerformed

