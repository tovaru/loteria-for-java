    private void ajustarMarco() {
        cartaGrafica1.setImagenDir(new File(marco));
        cartaGrafica1.ajustar();
        this.getRootPane().setDefaultButton(botonOK);
    }

    public void agregarMenu(PanelDeMenu componente) {
        componente.setName("Menú + " + menus.size());

        //Registrar éste como el invocador del panel
        componente.padre = this;
        //Agregar al arreglo de menús
        menus.add(componente);
        //Agregar al frame principal
        opciones.add(componente, componente.getName());
    }

    public void quitarMenu(PanelDeMenu componente) {
        //Eliminar padre
        componente.padre = null;
        //Eliminar del arreglo
        menus.remove(componente);
        //Eliminar del frame principal
        opciones.remove(componente);
    }

    public synchronized void ejecutar() throws InterruptedException {
        setVisible(true);
        if (terminado) {
            terminado = false;
            botonOK.doClick();
        }
        while (!terminado) {
            wait();
        }
    }

    private synchronized void terminar() {
        notifyAll();
        terminado = true;
    }

    public JButton getBotonAtras() {
        return botonAtras;
    }

    
